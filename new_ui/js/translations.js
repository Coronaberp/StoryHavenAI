let activeTranslations = null;

function t(key, fallback) {
  const source = fallback !== undefined ? fallback : (UI_STRINGS[key] || key);
  if (!activeTranslations) return source;
  return activeTranslations[key] || source;
}

const RTL_LANGUAGES = ["Arabic", "Hebrew"];

function applyUiDirection(lang) {
  const isRtl = RTL_LANGUAGES.some((l) => l.toLowerCase() === (lang || "").trim().toLowerCase());
  document.documentElement.dir = isRtl ? "rtl" : "ltr";
}

function dirMark(ltr, rtl) {
  return document.documentElement.dir === "rtl" ? rtl : ltr;
}

function applyTranslations() {
  document.querySelectorAll("[data-translate]").forEach(el => {
    el.textContent = t(el.dataset.translate);
  });
  document.querySelectorAll("[data-translate-title]").forEach(el => {
    el.title = t(el.dataset.translateTitle);
  });
  document.querySelectorAll("[data-translate-aria]").forEach(el => {
    el.setAttribute("aria-label", t(el.dataset.translateAria));
  });
  document.querySelectorAll("[data-translate-tooltip]").forEach(el => {
    el.dataset.tooltip = t(el.dataset.translateTooltip);
  });
}

async function loadUiTranslations(strings, targetLang) {
  const lang = (targetLang || "").trim();
  applyUiDirection(lang);
  if (!lang || lang.toLowerCase() === "english" || !strings) {
    activeTranslations = null;
    applyTranslations();
    return null;
  }
  const cacheKey = "ui_translations:" + lang.toLowerCase();
  const cached = store.get(cacheKey, null);
  if (cached) {
    activeTranslations = cached;
    applyTranslations();
  }
  try {
    const res = await api("/api/ui-translations", { method: "POST", body: JSON.stringify({ lang, strings }) });
    if (res && res.strings) {
      activeTranslations = res.strings;
      store.set(cacheKey, activeTranslations);
      applyTranslations();
    }
  } catch (e) {
    console.warn("loadUiTranslations failed", e);
  }
  return activeTranslations;
}

let translationsLoaded = false;

async function initInterfaceTranslations() {
  if (translationsLoaded) return;
  const lang = (ME && ME.interface_language || "").trim();
  applyUiDirection(lang);
  if (!lang || lang.toLowerCase() === "english") return;
  translationsLoaded = true;
  await loadUiTranslations(UI_STRINGS, lang);
}
